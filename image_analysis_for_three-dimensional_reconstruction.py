import cv2
import numpy as np
from PIL import Image
from sklearn.cluster import KMeans

# Load the image
image = Image.open('/content/drive/MyDrive/image1.jpg')

# Convert the image to a NumPy array
image_np = np.array(image)

# Convert the image to RGB
image_rgb = cv2.cvtColor(image_np, cv2.COLOR_BGR2RGB)

# Reshape the image into a one-dimensional array of pixels
pixels = image_rgb.reshape(-1, 3)

# K-Means clustering
k = 5  # Number of clusters
kmeans = KMeans(n_clusters=k, random_state=0)
labels = kmeans.fit_predict(pixels)

# Create a mask for each cluster
masks = []
for i in range(k):
    mask = np.zeros_like(labels, dtype=np.uint8)
    mask[labels == i] = 255
    masks.append(mask.reshape(image_rgb.shape[:2]))

# Apply the masks to the image
segmented_images = []
for i, mask in enumerate(masks):
    segmented_image = cv2.bitwise_and(image_rgb, image_rgb, mask=mask)
    segmented_images.append(segmented_image)

# Display the images with the highlighted clusters
for i, segmented_image in enumerate(segmented_images):
    # Print information about each image
    print(f"Segmented Image {i+1}")
    print(f"Cluster Label: {i}")
    print(f"Color: {kmeans.cluster_centers_[i]}")
    Image.fromarray(segmented_image).show()

