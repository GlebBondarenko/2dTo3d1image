# K-Means clustering
k = 5  # Number of clusters
kmeans = KMeans(n_clusters=k, random_state=0)
labels = kmeans.fit_predict(pixels)

# Create a mask for each cluster
masks = []
for i in range(k):
    mask = np.zeros_like(labels, dtype=np.uint8)
    mask[labels == i] = 255
    masks.append(mask.reshape(image_rgb.shape[:2]))

# Apply the masks to the image
segmented_images = []
for mask in masks:
    segmented_image = cv2.bitwise_and(image_rgb, image_rgb, mask=mask)
    segmented_images.append(segmented_image)

# Convert the masks to grayscale
gray_masks = [cv2.cvtColor(mask, cv2.COLOR_RGB2GRAY) for mask in segmented_images]

# Apply the Watershed algorithm to separate the objects
markers = np.zeros_like(gray_masks[0], dtype=np.int32)
for i, mask in enumerate(gray_masks):
    _, thresh = cv2.threshold(mask, 0, 255, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU)
    sure_fg = cv2.erode(thresh, None, iterations=3)
    _, markers = cv2.connectedComponents(sure_fg)
    markers += 1
    markers[mask == 0] = 0

# Apply the Watershed algorithm to the original image
image_copy = image_np.copy()
cv2.watershed(image_copy, markers)
image_copy[markers == -1] = [255, 0, 0]  # Mark the object boundaries

# Display each object separately
for i, mask in enumerate(masks):
    object_image = cv2.bitwise_and(image_rgb, image_rgb, mask=mask)
